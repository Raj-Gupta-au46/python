# typecasting
# conversion of one datatype into other known as typecasting
# type of typecasting
# implicit by python automatically
# explicit  by own or by programmer


# this is explicit example
a="1"
c=1
d=2
b="2"
# print(a+b)  give output 12
# for adding two integer
print(int(a)+int(b)) 
print(c+d)

# implicit example
f= 10.9
g=8
print(f+g) 
# the output will be float g =  8.0
